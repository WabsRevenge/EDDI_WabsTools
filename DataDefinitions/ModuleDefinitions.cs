        private static string NormalizedEDName(string rawEDName)
        {
            return rawEDName.ToLowerInvariant()
                .Replace("$", "") // Header for types from repair events
                .Replace("_name;", "") // Trailer for types from repair events
                .Replace("_Name;", "") // Trailer for types from repair events
                ;
        }

        public static Module FromEDName(string rawEDName, object rawData = null)
        {
            if (rawEDName == null || rawEDName == "Null") { return null; }
            string edName = NormalizedEDName(rawEDName);
            Module module = ResourceBasedLocalizedEDName<Module>.FromEDName(edName);
            if (module == null)
            {
                // Unknown module; report the full object if we can so that we can update the definitions
                if (rawData != null)
                {
                    Logging.Error("Unknown module edname: " + rawEDName, rawData);
                }
                else
                {
                    Logging.Warn("Unknown module edname: " + rawEDName);
                }
            }
            return module;
        }

        public static Module FromOutfittingInfo(OutfittingInfoItem item)
        {
            try
            {
                Logging.Debug($"Converting OutfittingInfoItem to Module: ", item);
                Module module = new Module(FromEDName(item.edName, item) ?? new Module());
                if (module.invariantName == null)
                {
                    // Create a basic module & supplement from the info available
                    module = new Module( item.edName, item.edName, -1, "", item.buyPrice);
                }
                module.price = item.buyPrice;

                return module;
            }
            catch (System.Exception ex)
            {
                Logging.Error($"Failed to parse OutfittingInfoItem.", ex);
            }
            return null;
        }
